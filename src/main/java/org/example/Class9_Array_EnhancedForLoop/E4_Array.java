package org.example.Class9_Array_EnhancedForLoop;

public class E4_Array {
    public static void main(String[] args) {

        int [] numbers={10,25,45,66,85,100};
        for (int i=5; i>=0; i--){
            System.out.println(i+ " " + numbers[i] + " ");
            // this will print in reverse

        }

    }//end of main

}//end of class
