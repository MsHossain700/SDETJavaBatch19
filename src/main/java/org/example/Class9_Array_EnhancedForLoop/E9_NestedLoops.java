package org.example.Class9_Array_EnhancedForLoop;

public class E9_NestedLoops {
    public static void main(String[] args) {


        for (int j=0; j<5; j++){
            int i=0;
            while (i<8) {
                System.out.print("+");
                i++;
            }
            System.out.println();
        }


    }//end of main
}//end of class
