package org.example.Class12_StringBuilder;

public class Task_4 {

    /*
    Create a program that will take a number and print whether the
    number is even or odd
     */

    void evenOrOdd (int num1){

        if (num1 % 2 == 0) {
            System.out.println(num1 + " is a even number");
        }else {
            System.out.println(num1 + " is a odd number");
        }

    }
}//end of main
