package org.example.Class6;

public class Task_2_Loop {
    public static void main(String[] args) {
        // write a while loop to print numbers from 10 to 1

        int num=10;
        while(num>0){
            System.out.println(num);

            num--;// decreases from "num" by 1 every iteration

        }//end of while loop
    }

}
