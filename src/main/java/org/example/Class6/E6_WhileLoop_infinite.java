package org.example.Class6;

public class E6_WhileLoop_infinite {
    public static void main(String[] args) {

        int num=10;
        while(num>0){//since "num" 10 is greater than 0 it will
            // continue forever
            System.out.println(num);
            num++;

        }//end of while loop


    }//end of main

}//end of class

