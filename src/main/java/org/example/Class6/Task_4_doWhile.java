package org.example.Class6;

public class Task_4_doWhile {
    public static void main(String[] args) {

        //write a do while loop to print numbers from 10 to 1

        double num = 10;

        do {
            System.out.println(num);
            num--;
        } while (num >= 1);


    }//end of main
}//end of class
