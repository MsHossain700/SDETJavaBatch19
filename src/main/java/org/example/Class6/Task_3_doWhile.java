package org.example.Class6;

public class Task_3_doWhile {
    public static void main(String[] args) {

        //write a do while loop to print numbers from 1 to 10

        double num = 1;

        do {
            System.out.println(num);
            num++;
        } while (num <= 10);


    }//end of main
}//end of class
