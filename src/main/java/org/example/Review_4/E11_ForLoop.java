package org.example.Review_4;

public class E11_ForLoop {
    public static void main(String[] args) {

        //for loop is best when number of iteration is known
        //this code will print only even numbers because of (i+=2)
        for (int i=0; i<=50;i+=2){
            System.out.println(i);
        }

    }//end of main
}//end of class
