package org.example.Review_4;

public class E10_ForLoop {
    public static void main(String[] args) {

        //for loop is best when number of iteration is known
        //in this code i starts at 50 and prints up to 65 because of <=

        for (int i=50; i<=65;i++){
            System.out.println(i);
        }

    }//end of main
}//end of class
