package org.example.Class3;

public class E10conditionalStatements {
    public static void main(String[] args) {

        //conditional statements can be just IF statements.
        int number = 10;
        if (number > 0) {
            System.out.println("The number is positive.");
        }//end of if

        //conditional statements can also be if else statement
        int x = 2;

        if (x > 1) {
            System.out.println("x is greater than 1");
        } else {
            System.out.println("x is not greater than 1");
        }//end of if else


    }//end of main

}//end of class
