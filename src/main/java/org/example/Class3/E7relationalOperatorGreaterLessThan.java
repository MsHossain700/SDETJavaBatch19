package org.example.Class3;

public class E7relationalOperatorGreaterLessThan {
    public static void main(String[] args) {

        //Greater Than >
        int number=100;
        int number2=20;
        System.out.println(number>number2);// passes a boolean answer. true or false
        boolean result=number>number2;//works the same way
        System.out.println(result);

        //Less Than <
        System.out.println(number<number2);
        boolean result2=number<number2;
        System.out.println(result2);



    }//end of main

}//end of class
