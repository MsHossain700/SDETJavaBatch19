package org.example.Class7;

public class E5_Arrays {
    public static void main(String[] args) {

        String [] names= {"Lana","Mat","Maggy","Noodles","James"};

        //write a for loop to print the array

        for (int i=0;i<5;i++){
            System.out.println(names[i]);

        }//end of for loop

        //write a while loop to print the array
        int i=0;
        while (i<5){
            System.out.println(names[i]);
            i++;
        }//end of while loop

    }//end of main

}//end of class
